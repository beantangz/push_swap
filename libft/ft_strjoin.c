#include "libft.h"

char	*ft_strjoin(char const *s1, char const *s2)
{
	size_t	lenfinal;
	char	*str;
	int		i;

	i = 0;
	lenfinal = ft_strlen((char *)s1) + ft_strlen((char *)s2);
	str = malloc(lenfinal + 1);
	if (!str)
		return (NULL);
	while (*s1)
		str[i++] = *s1++;
	while (*s2)
		str[i++] = *s2++;
	str[i] = '\0';
	return (str);
}
